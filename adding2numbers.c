//ADD TWO NUMBERS USING INTEGERS 
#include <stdio.h>
int main()
{
 int num1;
 float num2,res;
 scanf("%d%f",&num1,&num2);
 res=num1+num2;
 printf("addition of %d and %f is %f",num1,num2,res);
}
 